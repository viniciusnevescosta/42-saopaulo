#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	size_t	s_len;
	char	*heap;

	if (!s)
		return (NULL);
	s_len = ft_strlen(s);
	if (start >= s_len)
		return (ft_strdup(""));
	if (len > s_len - start)
		len = s_len - start;
	heap = malloc(sizeof(char) * (len + 1));
	if (!heap)
		return (NULL);
	ft_strlcpy(heap, s + start, len + 1);
	return (heap);
}
